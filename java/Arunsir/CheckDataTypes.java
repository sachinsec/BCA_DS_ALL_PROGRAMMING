public class CheckDataTypes {
    public static void main(String[] arg) {
        boolean b2 = true;
        boolean b1 = false;
        System.out.println("Boolean values: ");
        System.out.println("b1: " + b1);
        System.out.println("b2: " + b2);
        byte by1 = 10;
        byte by2 = 120; // byte range -128 to 127
        System.out.println("Byte values:");
        System.out.println("by1: " + by1);
        System.out.println("by2: " + by2);
        short s1 = 1000;
        short s2 = 32767; // if we assing s2 = 32767 then show error because short range -32,768 to 32,767
        System.out.println("Shorts values:");
        System.out.println("s1: " + s1);
        System.out.println("s2: " + s2);

        // INteger data type
        int i1 = 200000;
        int i2 = 328249383;
        System.out.println("Int data type:");
        System.out.println("i1: " + i1);
        System.out.println("i2: " + i2);

        // Long data type
        long l1 = 34344439845843L;
        long l2 = 3240238384239098L;
        System.out.println("Long data type:");
        System.out.println("l1: " + l1);
        System.out.println("l2: " + l2);

        // float data type

        float f1 = 2343.3233344f;
        float f2 = 2.423343083243242f;
        System.out.println("Float data type:");
        System.out.println("f1: " + f1);
        System.out.println("f2: " + f2);

        // Double data type

        double d1 = 23.234234233243298;
        double d2 = 23.92387493243924398472;
        System.out.println("Double data type:");
        System.out.println("d1: " + d1);
        System.out.println("d2: " + d2);

        // Char Data type
        char c1 = 'S';
        char c2 = 'R';
        System.out.println("Char data type:");
        System.out.println("C1: " + c1);
        System.out.println("C2: " + c2);

        // String data type
        String st1 = "Sachin";
        String st2 = "Arjun";
        System.out.println("String data type:");
        System.out.println("S1: " + st1);
        System.out.println("S2: " + st2);

        //

    }

}
